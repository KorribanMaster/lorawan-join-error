/// Event handler for BLE advertisements - processes Victron device data
struct VictronEventHandler {
    channel: &'static Channel<CriticalSectionRawMutex, Option<DeviceData>, 1>,
}

impl EventHandler for VictronEventHandler {
    fn on_adv_reports(&self, mut it: LeAdvReportsIter<'_>) {
        let scanner = VictronScanner::new(&ENCRYPTION_KEYS);

        while let Some(Ok(report)) = it.next() {
            // Get raw advertisement data
            let adv_data = report.data;

            // Parse through advertisement data to find manufacturer specific data (type 0xFF)
            let mut i = 0;
            while i < adv_data.len() {
                if i + 1 >= adv_data.len() {
                    break;
                }

                let length = adv_data[i] as usize;
                if length == 0 || i + length >= adv_data.len() {
                    break;
                }

                let ad_type = adv_data[i + 1];

                // Check for manufacturer specific data (0xFF)
                if ad_type == 0xFF && length >= 3 {
                    let data_start = i + 2;
                    let data_end = i + 1 + length;
                    let manufacturer_data = &adv_data[data_start..data_end];

                    if manufacturer_data.len() >= 3 {
                        let company_id =
                            u16::from_le_bytes([manufacturer_data[0], manufacturer_data[1]]);

                        if company_id == VICTRON_MANUFACTURER_ID {
                            info!(
                                "Found Victron device: {:x}, RSSI: {} dBm",
                                report.addr, report.rssi
                            );

                            // Parse manufacturer data (skip company ID)
                            match scanner.parse_manufacturer_data(&manufacturer_data[2..]) {
                                Ok(device_data) => {
                                    // Log detailed device data
                                    match &device_data {
                                        DeviceData::AcCharger(data) => {
                                            info!("AC Charger:");
                                            if let Some(v) = data.output_voltage1 {
                                                info!("  Output1: {} V", v as i32);
                                            }
                                            if let Some(state) = data.charge_state {
                                                info!("  State: {:?}", state);
                                            }
                                        }
                                        DeviceData::BatteryMonitor(data) => {
                                            info!("Battery Monitor:");
                                            info!("  Voltage: {} V", data.voltage as i32);
                                            info!("  Current: {} A", data.current as i32);
                                            if let Some(soc) = data.soc {
                                                info!("  SOC: {} %", soc as i32);
                                            }
                                            info!("  Consumed: {} Ah", data.consumed_ah as i32);
                                        }
                                        DeviceData::BatterySense(data) => {
                                            info!("Battery Sense:");
                                            info!("  Voltage: {} V", data.voltage as i32);
                                            if let Some(temp) = data.temperature {
                                                info!("  Temperature: {} C", temp as i32);
                                            }
                                        }
                                        DeviceData::DcDcConverter(data) => {
                                            info!("DC-DC Converter:");
                                            info!("  Input: {} V", data.input_voltage as i32);
                                            info!("  Output: {} V", data.output_voltage as i32);
                                            if let Some(state) = data.charge_state {
                                                info!("  State: {:?}", state);
                                            }
                                        }
                                        DeviceData::DcEnergyMeter(data) => {
                                            info!("DC Energy Meter:");
                                            if let Some(v) = data.voltage {
                                                info!("  Voltage: {} V", v as i32);
                                            }
                                            if let Some(c) = data.current {
                                                info!("  Current: {} A", c as i32);
                                            }
                                        }
                                        DeviceData::Inverter(data) => {
                                            info!("Inverter:");
                                            if let Some(v) = data.battery_voltage {
                                                info!("  Battery: {} V", v as i32);
                                            }
                                            if let Some(p) = data.ac_apparent_power {
                                                info!("  AC Power: {} VA", p);
                                            }
                                        }
                                        DeviceData::LynxSmartBMS(data) => {
                                            info!("Lynx Smart BMS:");
                                            if let Some(v) = data.voltage {
                                                info!("  Voltage: {} V", v as i32);
                                            }
                                            if let Some(soc) = data.soc {
                                                info!("  SOC: {} %", soc as i32);
                                            }
                                        }
                                        DeviceData::OrionXS(data) => {
                                            info!("Orion XS:");
                                            info!(
                                                "  Input: {} V @ {} A",
                                                data.input_voltage as i32, data.input_current as i32
                                            );
                                            info!(
                                                "  Output: {} V @ {} A",
                                                data.output_voltage as i32, data.output_current as i32
                                            );
                                            if let Some(state) = data.charge_state {
                                                info!("  State: {:?}", state);
                                            }
                                        }
                                        DeviceData::SmartBatteryProtect(data) => {
                                            info!("Smart Battery Protect:");
                                            if let Some(v) = data.input_voltage {
                                                info!("  Input: {} V", v as i32);
                                            }
                                            if let Some(state) = data.output_state {
                                                info!("  Output: {:?}", state);
                                            }
                                        }
                                        DeviceData::SmartLithium(data) => {
                                            info!("Smart Lithium:");
                                            if let Some(v) = data.battery_voltage {
                                                info!("  Voltage: {} V", v as i32);
                                            }
                                            if let Some(status) = data.balancer_status {
                                                info!("  Balancer: {:?}", status);
                                            }
                                        }
                                        DeviceData::SolarCharger(data) => {
                                            info!("Solar Charger:");
                                            info!(
                                                "  Battery: {} V @ {} A",
                                                data.battery_voltage as i32,
                                                data.battery_current as i32
                                            );
                                            info!("  PV Power: {} W", data.pv_power);
                                            info!("  Yield today: {} Wh", data.yield_today);
                                            if let Some(state) = data.charge_state {
                                                info!("  State: {:?}", state);
                                            }
                                        }
                                        DeviceData::VEBus(data) => {
                                            info!("VE.Bus:");
                                            if let Some(v) = data.battery_voltage {
                                                info!("  Battery: {} V", v as i32);
                                            }
                                            if let Some(soc) = data.soc {
                                                info!("  SOC: {} %", soc);
                                            }
                                        }
                                    }

                                    // Send to channel (non-blocking, overwrites old data)
                                    self.channel.try_send(Some(device_data)).ok();
                                }
                                Err(e) => {
                                    warn!("Failed to parse Victron data: {:?}", e);
                                }
                            }
                        }
                    }
                }

                i += 1 + length;
            }
        }
    }
}

/// LoRaWAN task - handles network joining and data transmission
#[embassy_executor::task]
async fn lorawan_task(
    victron_channel: &'static Channel<CriticalSectionRawMutex, Option<DeviceData>, 1>,
) {
    info!("LoRaWAN task starting...");

    // Wait a bit for system to initialize
    Timer::after(Duration::from_secs(2)).await;

    // Steal peripherals for LoRa radio (SPI already initialized by main)
    // We'll initialize SPI here since it's needed only for LoRaWAN
    let peripherals = unsafe { esp_hal::peripherals::Peripherals::steal() };

    let nss = Output::new(peripherals.GPIO8, Level::High, OutputConfig::default());
    let sclk = peripherals.GPIO9;
    let mosi = peripherals.GPIO10;
    let miso = peripherals.GPIO11;

    let reset = Output::new(peripherals.GPIO12, Level::Low, OutputConfig::default());
    let busy = Input::new(peripherals.GPIO13, InputConfig::default());
    let dio1 = Input::new(peripherals.GPIO14, InputConfig::default());

    let spi = Spi::new(
        peripherals.SPI2,
        Config::default()
            .with_frequency(Rate::from_khz(100))
            .with_mode(Mode::_0),
    )
    .unwrap()
    .with_sck(sclk)
    .with_mosi(mosi)
    .with_miso(miso)
    .into_async();

    // Initialize the static SPI bus
    let spi_bus = SPI_BUS.init(Mutex::new(spi));
    let spi_device = embassy_embedded_hal::shared_bus::asynch::spi::SpiDevice::new(spi_bus, nss);

    // Create the SX126x configuration
    let sx126x_config = sx126x::Config {
        chip: Sx1262,
        tcxo_ctrl: Some(TcxoCtrlVoltage::Ctrl1V7),
        use_dcdc: true,
        rx_boost: false,
    };

    // Create the radio instance
    let iv = GenericSx126xInterfaceVariant::new(reset, dio1, busy, None, None).unwrap();
    let lora = LoRa::new(Sx126x::new(spi_device, iv, sx126x_config), true, Delay)
        .await
        .unwrap();

    let radio: LorawanRadio<_, _, MAX_TX_POWER> = lora.into();
    let region: region::Configuration = region::Configuration::new(LORAWAN_REGION);
    let rng = esp_hal::rng::Rng::new();
    let mut rng2 = esp_hal::rng::Rng::new(); // Separate RNG for delay generation
    let timer = SimpleTimer::new();
    let mut device: Device<_, Crypto, _, _> = Device::new(region, radio, timer, rng);

    let mut deveui = DEVEUI.unwrap_or(DEFAULT_DEVEUI);
    deveui.reverse();
    let appeui = APPEUI.unwrap_or(DEFAULT_APPEUI);
    let appkey = APPKEY.unwrap_or(DEFAULT_APPKEY);

    info!("LoRaWAN Credentials:");
    info!("  DevEUI: {:#x}", deveui);
    info!("  AppEUI: {:#x}", appeui);
    info!("  AppKey: {:#x}", appkey);
    info!("Joining LoRaWAN network");

    let join_mode = JoinMode::OTAA {
        deveui: DevEui::from(deveui),
        appeui: AppEui::from(appeui),
        appkey: AppKey::from(appkey),
    };

    let mut retries = 0;

    loop {
        let join_result = device.join(&join_mode).await;
        match join_result {
            Ok(JoinResponse::JoinSuccess) => {
                info!("LoRaWAN network joined");
                break;
            }
            Ok(JoinResponse::NoJoinAccept) => {
                warn!("LoRaWAN network did not accept");
            }
            Err(e) => {
                error!("Error joining LoRaWAN network: {}", e)
            }
        }
        let delay = generate_delay(&mut rng2, retries);

        info!("Join failed. Retrying in {} seconds..", delay);
        Timer::after(Duration::from_secs(delay.into())).await;
        retries += 1;
    }

    info!("LoRaWAN network joined!");

    let mut counter = 0u32;
    let mut payload_buffer = [0u8; 51]; // EU868 max payload

    loop {
        // Try to get latest Victron data from channel (non-blocking)
        let victron_data = victron_channel.try_receive().ok().flatten();

        let (data, fport) = if let Some(device_data) = victron_data {
            // Pack Victron data into binary format
            let len = victron_payload::pack_device_data(&device_data, &mut payload_buffer);

            if len > 0 {
                info!("Sending Victron data: {} bytes", len);
                (&payload_buffer[..len], 2) // Port 2 for Victron data
            } else {
                // Fallback if packing failed
                warn!("Failed to pack Victron data, sending test message");
                (b"Hello LoRaWAN" as &[u8], 2)
            }
        } else {
            // No Victron data available, send test message
            info!("No Victron data available, sending test message #{}", counter);
            (b"Hello LoRaWAN" as &[u8], 2)
        };

        let confirmed = false; // Use unconfirmed uplink

        // Send the message
        match device.send(data, fport, confirmed).await {
            Ok(response) => {
                info!("Message sent successfully: {:?}", response);

                // Check for downlink messages
                if let Some(downlink) = device.take_downlink() {
                    info!("Received downlink on port {}, {} bytes",
                          downlink.fport, downlink.data.len());
                }
            }
            Err(e) => {
                error!("Failed to send message: {}", e);
            }
        }

        counter += 1;

        // Wait 60 seconds before next transmission (respect duty cycle)
        info!("Waiting 60 seconds before next transmission...");
        Timer::after(Duration::from_secs(60)).await;
    }
}

#[esp_rtos::main]
async fn main(spawner: Spawner) -> ! {
    // generator version: 1.0.1

    let config = esp_hal::Config::default().with_cpu_clock(CpuClock::max());
    let peripherals = esp_hal::init(config);

    esp_alloc::heap_allocator!(#[esp_hal::ram(reclaimed)] size: 73744);

    let timg0 = TimerGroup::new(peripherals.TIMG0);
    esp_rtos::start(timg0.timer0);

    info!("Embassy initialized!");

    // Initialize Victron data channel
    let victron_channel = VICTRON_DATA_CHANNEL.init(Channel::new());

    // let radio_init = esp_radio::init().expect("Failed to initialize Wi-Fi/BLE controller");
    // // find more examples https://github.com/embassy-rs/trouble/tree/main/examples/esp32
    // let transport = BleConnector::new(&radio_init, peripherals.BT, Default::default()).unwrap();
    // let ble_controller = ExternalController::<_, 20>::new(transport);

    // let resources = HOST_RESOURCES.init(HostResources::new());
    //
    // // Create scanner address for BLE
    // let address: Address = Address::random([0xff, 0x8f, 0x1b, 0x05, 0xe4, 0xff]);
    // let stack = trouble_host::new(ble_controller, resources)
    //     .set_random_address(address);
    // let Host {
    //     central, mut runner, ..
    // } = stack.build();

    let radio_init = esp_radio::init().expect("Failed to initialize Wi-Fi/BLE controller");
    let transport = BleConnector::new(&radio_init, peripherals.BT, Default::default()).unwrap();
    let ble_controller = ExternalController::<_, 20>::new(transport);
    
    // Create BLE host resources
    let mut resources: HostResources<DefaultPacketPool, CONNECTIONS_MAX, L2CAP_CHANNELS_MAX> =
        HostResources::new();

    // Use a random address for scanning
    let address: Address = Address::random([0xff, 0x8f, 0x1b, 0x05, 0xe4, 0xff]);
    info!("Scanner address = {:?}", address);

    let stack = trouble_host::new(ble_controller, &mut resources).set_random_address(address);

    let Host {
        central,
        mut runner,
        ..
    } = stack.build();
    info!("Bluetooth initialized with scanning support");

    // Spawn LoRaWAN task
    spawner.spawn(lorawan_task(victron_channel)).unwrap();
    info!("LoRaWAN task spawned");

    // Run BLE using join pattern (keeps everything in main's stack frame)
    // Create event handler for processing advertisements
    let handler = VictronEventHandler {
        channel: victron_channel,
    };
    let mut scanner = Scanner::new(central);

    let scan_config = ScanConfig {
        active: true,
        phys: PhySet::M1,
        interval: Duration::from_millis(200),
        window: Duration::from_millis(100),
        ..Default::default()
    };

    info!("Starting BLE scan for Victron devices...");

    // Run BLE runner and scanner concurrently using join
    // This never returns, keeping the main function alive
    let _ = join(
        runner.run_with_handler(&handler),
        async {
            loop {
                info!("Initiating BLE scan...");
                match scanner.scan(&scan_config).await {
                    Ok(_session) => {
                        info!("BLE scan session started successfully");

                        // Keep the session alive - scanning continues as long as _session is in scope
                        // The scan will run until the session is dropped or an error occurs
                        loop {
                            Timer::after(Duration::from_secs(10)).await;
                        }
                    }
                    Err(e) => {
                        error!("Failed to start BLE scan: {:?}", e);
                        Timer::after(Duration::from_secs(5)).await;
                        // Loop will retry scanning
                    }
                }
            }
        }
    ).await;

    // This point is never reached
    unreachable!()
}
